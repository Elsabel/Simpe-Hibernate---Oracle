    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        String kolom = "";
        switch (cmb_kategori.getSelectedIndex()) {
            case 0:
                kolom = "jobId";
                break;
            case 1:
                kolom = "jobTitle";
                break;
            case 2:
                kolom = "minSalary";
                break;
            case 3:
                kolom = "maxSalary";
                break;
            default:
                throw new AssertionError();
        }
        jgc.bindingsearch(tbl_jobs, header, kolom,
                txt_search.getText());
    }//GEN-LAST:event_btn_searchActionPerformed

