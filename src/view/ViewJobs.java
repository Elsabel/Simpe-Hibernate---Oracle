    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
         boolean hasil = false;
        if (!txt_jobid.isEnabled()) {
            hasil = jgc.update(
                    txt_jobid.getText(),
                    txt_jobTitle.getText(),
                    Integer.parseInt(txt_mimSalary.getText()),
                    Integer.parseInt(txt_maxSalary.getText()));
        } else {
            hasil = jgc.insert(
                    txt_jobid.getText(),
                    txt_jobTitle.getText(),
                    Integer.parseInt(txt_mimSalary.getText()),
                    Integer.parseInt(txt_maxSalary.getText()));
        }
        String pesan = "gagal menginputkan data";
        if (hasil) {
            pesan = "berhasil menginputkan data";
        }
        JOptionPane.showMessageDialog(this, pesan);
        reset();
        jgc.bindingall(tbl_jobs, header);
        
    }//GEN-LAST:event_btn_saveActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int i = JOptionPane.showConfirmDialog(this, "Apakah Anda Yakin Ingin dihapus?");
        if (i == 0) {
            String pesan = "Gagal hapus";
            boolean hasil = jgc.delete(txt_jobid.getText());
            if (hasil) {
                pesan = "Hore Berhasil";
            }
            JOptionPane.showMessageDialog(this, pesan);
            jgc.bindingall(tbl_jobs, header);

        }
        reset();
    }//GEN-LAST:event_btn_deleteActionPerformed

