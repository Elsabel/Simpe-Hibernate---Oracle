 public void bindingTabel() {

        String header[] = {"Job ID", "Job Title", "Minimum Salary", "Maximum Salary"};
        List<Object> datas = new JobsDAO().getAll();
        Object[][] data = new Object[datas.size()][3];
        Jobs jobs= null;
        for (int i = 0; i < datas.size(); i++) {
             jobs = (Jobs) datas.get(i);
            data[i][0] = jobs.getJobId();
            data[i][1] = jobs.getJobTitle();
            data[i][2] = jobs.getMinSalary();
            data[i][3] = jobs.getMaxSalary();
        }
        DefaultTableModel model = new DefaultTableModel(data, header);
        tbl_jobs.setModel(model);
    }
